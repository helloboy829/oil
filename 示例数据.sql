-- 查看订单统计
SELECT
    payment_type AS '支付方式',
    COUNT(*) AS '订单数',
    CONCAT('¥', FORMAT(SUM(total_amount), 2)) AS '总金额'
FROM orders
GROUP BY payment_type
ORDER BY SUM(total_amount) DESC;

-- 查看月结客户未结算金额
SELECT
    customer_name AS '客户名称',
    COUNT(*) AS '订单数',
    CONCAT('¥', FORMAT(SUM(total_amount), 2)) AS '未结算金额'
FROM orders
WHERE payment_type = '月结' AND payment_status = '未结算'
GROUP BY customer_name
ORDER BY SUM(total_amount) DESC;

